    public static int longestCommonSubsequence(String s, String r) {
        int lengthS = s.length();
        int lengthR = r.length();

        int dp[][] = new int[lengthS + 1][lengthR + 1];

        for (int i = 0; i <= s.length(); i++) {
            for (int j = 0; j <= r.length(); j++) {
                if (i == 0 || j == 0) {
                    dp[i][j] = 0;
                } else if (s.charAt(i - 1) == r.charAt(j - 1)) {
                    dp[i][j] = 1 + dp[i - 1][j - 1];
                } else
                    dp[i][j] = Math.max(dp[i - 1][j], dp[i][j - 1]);
            }
        }

        return dp[r.length()][s.length()];
    }

